package cn.xinil.domain;

public class Spring {
    String spring;

    public String getSpring() {
        return spring;
    }

    public void setSpring(String spring) {
        this.spring = spring;
    }

    @Override
    public String toString() {
        return "Spring{" +
                "spring='" + spring + '\'' +
                '}';
    }
}
